# > < == != <= >=

print('Verificação de Operadores Relacionais')
print(f'8 é maior que 12? {8 > 12}')
print(f'5 é menor que 22? {5 < 22}')
print(f'5 é igual a 5? {5 == 5}')
print(f'15 é diferente de 15? {15 != 15}')
print(f'12 é maior ou igual a 11? {12 >= 11}')
print(f'12 é menor ou igual a 11? {12 <= 11}')
